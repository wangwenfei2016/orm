package com.xymn.connect.pinduoduo.entity;

public class PinduoduoSku {

	private String spec;
	private String sku_id;
	private String sku_quantity;
	private String outer_id;
	private String outer_goods_id;
	private String is_sku_onsale;

	public String getSpec() {
		return spec;
	}

	public void setSpec(String spec) {
		this.spec = spec;
	}

	public String getSku_id() {
		return sku_id;
	}

	public void setSku_id(String sku_id) {
		this.sku_id = sku_id;
	}

	public String getSku_quantity() {
		return sku_quantity;
	}

	public void setSku_quantity(String sku_quantity) {
		this.sku_quantity = sku_quantity;
	}

	public String getOuter_id() {
		return outer_id;
	}

	public void setOuter_id(String outer_id) {
		this.outer_id = outer_id;
	}

	public String getOuter_goods_id() {
		return outer_goods_id;
	}

	public void setOuter_goods_id(String outer_goods_id) {
		this.outer_goods_id = outer_goods_id;
	}

	public String getIs_sku_onsale() {
		return is_sku_onsale;
	}

	public void setIs_sku_onsale(String is_sku_onsale) {
		this.is_sku_onsale = is_sku_onsale;
	}

}
